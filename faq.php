          <h2>Who are we?</h2>
          <p>United Martial Arts Self Defense Academy (UMASDA) is a school for learning methods of Self Defense. Our name reflects our origins: United Martial Arts represents our coursework originating from several mother systems of training. Self Defense represents our training focus. Academy connotes a place of learning; our school is modeled on collegiate academic standards including written and oral exams in addition to the physical execution of coursework.</p>
          <h2>Why study here?</h2>
          <p>Pick up any phone book - you can find places to learn martial arts. We are here to provide a unique and different approach to the process. We have programs in place found nowhere else designed to improve knowledge acquisition and retention. Our motto, &quot;Serious Skills. Reasonable Time&quot; embodies our focus.</p>
          <h2>What are your rates?</h2>
          <p>With no contract and ability to attend as many classes during the month as you like, our rates are some of the lowest in the county. Please call the school directly for more information. The only restricted class is the Sunday night class, which is 301+ (Green Belts and higher) for Instructor Development. Private lessons are $30/30 minutes by appointment. The Kids &amp; Young Adults program are both taught via private lessons, with negotiable rates depending on training focus. Course Graduations are $50 for underbelts (101-302) and $200 for all levels of Black Belt (401+).</p>
          <h2>What makes UMASDA different?</h2>
          <p>Our focus is entirely on information transfer with the focus exclusively on Street Effective and Legal Self Defense. We teach nothing that does not meet both of those criteria. While training for tournaments and cage &quot;fights&quot; has value to those who enjoy that perspective, neither skill set lends itself easily to street altercations.</p>
          <h2>What do we do here?</h2>
          <p>We utilize state of the art technology with a progressive <a title="Curriculum.html" href="Curriculum.html">curriculum</a> designed at every stage to build upon the previous level's skill sets, principles, and concepts. This process enables the student to measure their progress against the goals for each course at any time interval they wish. (Weekly, monthly, quarterly, etc.)</p>
          <h2>I'm not in shape - can I still study?</h2>
          <p>Absolutely! Any physician will tell you that virtually any exercise is better than none. Granted - while being at a healthy weight and eating right will contribute a host of benefits and generally means people learn physical skills faster - it is by no means a requisite to learning! We do recommend you consult a physician before starting any athletic endeavor.</p>

